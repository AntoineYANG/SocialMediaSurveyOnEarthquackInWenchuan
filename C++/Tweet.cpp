string filt(const string& str) {
	string str_new = str;
	for (int i = 0; i < 26; i++) {
		while (str_new.find(syntax[i])!=-1)
			str_new = str_new.replace(str_new.find(syntax[i]), str_new.find(syntax[i])+syntax[i].length(), ",");
	}
	for (string stopword : stopwords) {
		while (str_new.find(stopword)!=-1)
			str_new = str_new.replace(str_new.find(stopword), str_new.find(stopword)+stopword.length(), ",");
	}

	return str_new;
}

